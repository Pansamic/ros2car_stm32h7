int uart_open(uart_t* pcb)
{
    void* p;
    size_t len;
    ringbuf_ret_t err;
    err = ringbuf_get_free_continuous_block(&pcb->rx_ringbuf, &p, &len);
    if(err != RINGBUF_OK)
    {
        return -1;
    }

    // dma receive initialization
    LL_USART_EnableDMAReq_RX(pcb->huart);
    LL_DMA_SetPeriphAddress(pcb->dma_rx, pcb->dma_rx_channel, (uint32_t)&pcb->huart->RDR);
    LL_DMA_SetMemoryAddress(pcb->dma_rx, pcb->dma_rx_channel, (uint32_t)p);
    LL_DMA_SetDataLength(pcb->dma_rx, pcb->dma_rx_channel, len);
    pcb->expect_dma_rx_len = len;
    LL_DMA_EnableIT_TC(pcb->dma_rx, pcb->dma_rx_channel);
    LL_DMA_EnableStream(pcb->dma_rx, pcb->dma_rx_channel);
    LL_USART_EnableIT_IDLE(pcb->huart);

    // dma transmission initialization
    LL_USART_EnableDMAReq_TX(pcb->huart);
    LL_DMA_SetPeriphAddress(pcb->dma_tx, pcb->dma_tx_channel, (uint32_t)&pcb->huart->TDR);
    LL_DMA_DisableIT_TC(pcb->dma_tx, pcb->dma_tx_channel);
    LL_DMA_DisableStream(pcb->dma_tx, pcb->dma_tx_channel);
    return 0;
}

int uart_close(uart_t* pcb)
{
    LL_USART_DisableDMAReq_RX(pcb->huart);
    LL_USART_DisableDMAReq_TX(pcb->huart);
    LL_DMA_DisableIT_TC(pcb->dma_rx, pcb->dma_rx_channel);
    LL_DMA_DisableStream(pcb->dma_rx, pcb->dma_rx_channel);
    LL_USART_DisableIT_IDLE(pcb->huart);
    LL_USART_DisableIT_TC(pcb->huart);
    return 0;
}

int uart_send(uart_t* pcb, void* pdata, size_t length)
{
    void* p;
    size_t len;
    ringbuf_ret_t err;
    while(pcb->tx_ringbuf.capacity - pcb->tx_ringbuf.size < length){}
    /**
     * @TODO: check whether `LL_DMA_IsEnabledStream()` can determine DMA
     * is transmitting or not.
     */
    if(LL_DMA_IsEnabledStream(pcb->dma_tx, pcb->dma_tx_channel))
    {
        /**
         * @note program will enters DMA interrupt handler as soon as 
         * DMA stream is disabled and the "Transmission Complete"
         * flag will be set as well.
         */
        LL_DMA_DisableStream(pcb->dma_tx, pcb->dma_tx_channel);
    }
    err = ringbuf_write_block(&pcb->tx_ringbuf, pdata, length);
    if(err != RINGBUF_OK)
    {
        LOG_ERROR("UART send error: ringbuf error:%d\n", err);
        return -1;
    }
    err = ringbuf_get_stuffed_continuous_block(&pcb->tx_ringbuf, &p, &len);
    if(err != RINGBUF_OK)
    {
        LOG_ERROR("UART send error: ringbuf error:%d\n", err);
        return -1;
    }
    __DSB();
    SCB_CleanInvalidateDCache();
    LL_DMA_DisableStream(pcb->dma_tx, pcb->dma_tx_channel);
    LL_DMA_SetMemoryAddress(pcb->dma_tx, pcb->dma_tx_channel, (uint32_t)p);
    LL_DMA_SetDataLength(pcb->dma_tx, pcb->dma_tx_channel, (uint32_t)len);
    LL_DMA_EnableIT_TC(pcb->dma_tx, pcb->dma_tx_channel);
    pcb->expect_dma_tx_len = len;
    LL_DMA_EnableStream(pcb->dma_tx, pcb->dma_tx_channel);
    return 0;
}

int uart_read(uart_t* pcb, void* pdata, size_t length, size_t* read_length)
{
    int err = ringbuf_get_block(&pcb->rx_ringbuf, pdata, length, read_length);
    return err;
}

/**
  * @brief  This function performs one random number generation.
  *
  * @retval uint32_t unsigned 32-bit random number
  */
uint32_t get_random_number(void)
{
  uint32_t rand_num = 0;

  // LL_RNG_EnableCondReset(RNG);
  // LL_RNG_SetHealthConfig(RNG,0x17590ABCU);
  // LL_RNG_SetHealthConfig(RNG,0x00007274U);
  // LL_RNG_DisableCondReset(RNG);

  /* Initialize random numbers generation */
  LL_RNG_Enable(RNG);

  /* Generate Random 32bit Numbers */
  /* Wait for DRDY flag to be raised */
  while (!LL_RNG_IsActiveFlag_DRDY(RNG));

  /* Check if error occurs */
  if ((LL_RNG_IsActiveFlag_CECS(RNG) ) || (LL_RNG_IsActiveFlag_SECS(RNG)))
  {
    return 0;
  }
  
  /* Otherwise, no error detected : Value of generated random number could be retrieved
    and stored in variable */
  rand_num = LL_RNG_ReadRandData32(RNG);

  /* Stop random numbers generation */
  LL_RNG_Disable(RNG);

  return rand_num;
}

